    protected function registroAngular(Request $request)
    {
        return User::create([
            'name' => $request->name,
            'lastname' => $request->lastname,
            'email' => $request->email,
            'password' => Hash::make($request->password),
            'respuesta' => strtolower($request->respuesta),
        ]);
    }

    protected function cambiarPassAngular(Request $request)
    {
        $user = \App\User::find($request->id);

        $user->update(['password' => Hash::make($request->pass)]);

        $message = 'Se cambio la contraseÃ±a';

        $response = Response::json([
            'message' => $message,
            'user' => $user
        ], 201);

        return $response;
    }
    
}
